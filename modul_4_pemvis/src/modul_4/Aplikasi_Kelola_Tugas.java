    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        
        matakuliah = txtNamaMatkul.getText();
        deadlineTugas = txtDeadline.getText();
        detailTugas = taDetail.getText();

        if (matakuliah.isEmpty() && deadlineTugas.isEmpty() && detailTugas.isEmpty() || matakuliah.isEmpty() || deadlineTugas.isEmpty() || detailTugas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan nilai yang valid! dan pastikan terisi semua", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {
            String tugas = (listTugas.size() + 1) + "." + matakuliah + " ( Deadline : " + deadlineTugas + ")";
            listTugas.addElement(tugas);
            listDetail.addElement(detailTugas);

            // Update model data untuk ListDaftarTugas
            ListDaftarTugas.setModel(listTugas);

            JOptionPane.showMessageDialog(this, "Data Telah diSimpan");

            txtNamaMatkul.setText("");
            txtDeadline.setText("");
            taDetail.setText("");
            
        }
      
        
    }//GEN-LAST:event_bSimpanActionPerformed

    private void bRiset2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRiset2ActionPerformed
        
        int konfirmasi = JOptionPane.showConfirmDialog(this, 
        "Anda yakin ingin mereset semua data tugas?", "Konfirmasi Reset", 
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            listTugas.clear();
            detailTugas = "";
            txtDetailTugas.setText(detailTugas);
        
        }
        
    }//GEN-LAST:event_bRiset2ActionPerformed

    private void bRisetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRisetActionPerformed
        
        if (!txtNamaMatkul.getText().isEmpty() || !txtDeadline.getText().isEmpty() || !taDetail.getText().isEmpty()) {
            int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Anda yakin ingin mereset semua kolom?", "Konfirmasi Reset", 
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (konfirmasi == JOptionPane.YES_OPTION) {
                txtNamaMatkul.setText("");
                txtDeadline.setText("");
                taDetail.setText("");
            }
        }
                
    }//GEN-LAST:event_bRisetActionPerformed
    
