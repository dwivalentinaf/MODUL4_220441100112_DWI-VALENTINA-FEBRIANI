    private void ListDaftarTugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListDaftarTugasMouseClicked
        
        if (evt.getClickCount() == 1) {
            int pilihIndex = ListDaftarTugas.locationToIndex(evt.getPoint());
            if (pilihIndex >= 0) {
                
                // Mendapatkan nilai dari nomer yang dipilih
                
                String pilihdetail = listDetail.getElementAt(pilihIndex);
                txtDetailTugas.setText(pilihdetail);
            }
        }
        
    }//GEN-LAST:event_ListDaftarTugasMouseClicked

    private void bRiset3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRiset3ActionPerformed
        
        int pilihIndex = ListDaftarTugas.getSelectedIndex();
        
        if (pilihIndex != -1) {
            listTugas.remove(pilihIndex);
            listDetail.removeElementAt(pilihIndex);

            // Menyusun ulang nomor urut di list tugas
            for (int i = pilihIndex; i < listTugas.size(); i++) {
                String tugas = listTugas.getElementAt(i);
                int noUrutBaru = Integer.parseInt(tugas.substring(0, tugas.indexOf("."))) - 1;
                String newTugas = noUrutBaru + tugas.substring(tugas.indexOf("."));
                listTugas.set(i, newTugas);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih tugas yang ingin dihapus terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        txtDetailTugas.setText(""); // Membersihkan area detail setelah menghapus

    }//GEN-LAST:event_bRiset3ActionPerformed

